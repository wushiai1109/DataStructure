    public int[][] allCellsDistOrder(int R, int C, int r0, int c0) {
        int[][] ret = new int[R * C][2];
        int dist = 0;
        int count = 0;
        //æ¨ªåæ ‡ä¸ºï¼šr0-rowDistæˆ–r0+rowDistï¼Œçºµåæ ‡ä¸ºï¼šc0-colDistæˆ–c0+colDist
        int[] factor = {-1, 1};

        while (count < R * C) {

            for (int rowDist = 0; rowDist <= dist; rowDist++) {
                int colDist = dist - rowDist;

                for (int i = 0; i < 2; i++) {
                    int row = r0 + factor[i] * rowDist;
                    for (int j = 0; j < 2; j++) {
                        int col = c0 + factor[j] * colDist;
                        //0<=row<R and 0<=col<C
                        if (row >= 0 && row < R && col >= 0 && col < C) {
                            ret[count][0] = row;
                            ret[count][1] = col;
                            count++;
                        }
                        if (colDist == 0) {
                            break;
                        }
                    }
                    if (rowDist == 0) {
                        break;
                    }
                }

            }

            dist++;

        }

        return ret;
    }
